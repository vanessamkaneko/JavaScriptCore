// Operadores lógicos (logical operators)

// São 2 valores booleanos, que quando verificados resultarão em V ou F

let pao 
let queijo 

// AND &&
console.log(pao && queijo) // só é true quando for let pao = true e let queijo = true, caso um deles seja false ou os dois sejam false, o resultado também é false - pois tem que ser um E o outro

// OR ||
console.log(pao || queijo) // só é false quando for let pao = false e let queijo = false, todas as outras situações serão true - pois pode ser um OU outro 

// NOT ! - negação; sempre que colocar na frente da variável, irei negar o valor dela

let pao = true
console.log(!pao) // vai dar como false





