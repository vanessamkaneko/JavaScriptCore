// incremento ++ (sempre equivale a +1)
let increment = 0 
increment++ 
console.log(increment)
// há diferença em incrementar antes (++increment) e depois (increment++) no console.log
// se eu colocasse console.log(increment++) daria 0, pois incremento após, e no próximo console.log(increment) vale 1 | console.log(++increment) daria 1, pois incrementa antes, e no próximo console.log(increment daria 1)

// decremento (vale -1)
let decrement = 0
decrement--
console.log(decrement)

console.log(decrement--) // dá 0
console.log(--decrement) // dá -1

// exponencial
console.log(3 ** 3) // 3 elevado a 3 = 9