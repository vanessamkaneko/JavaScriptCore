// arrow function - maneira mais compacta de se escrever uma function do tipo expression

const sayMyName = (name) => {
    console.log(name)
}

sayMyName ('Vanessa')

// a palavra chave function é retirada e no espaço entre () e {} coloca-se a arrow | lembrando que agora é preciso criar primeiro a função e depois executá-la, pois não tera o hoisting | geralmente é atribuída a uma const (constante) 